(ns ch.1.ex.1.10)

(defn A [x y]
  (cond (= y 0) 0
        (= x 0) (* 2 y)
        (= y 1) 2
        :else (A (dec x)
                 (A x ( dec y)))))

(A 1 10)
;; => 1024

(A 2 4)
;; => 65536

(A 3 3)
;; => 65536

(defn f [n]
  (A 0 n))

(f 1)
;; => 2

(f 2)
;; => 4

(f 3)
;; => 6

(f 4)
;; => 8

(defn g [n]
  (A 1 n))

(g 1)
;; => 2

(g 2)
;; => 4

(g 3)
;; => 8

(g 4)
;; => 16

